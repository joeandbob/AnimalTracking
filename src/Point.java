public class Point {
    private int r, c;

    public Point(int r, int c) {
        this.r = r;
        this.c = c;
    }

    public int getRow() {
        return r;
    }

    public void setRow(int r) {
        this.r = r;
    }

    public int getCol() {
        return c;
    }

    public void setCol(int c) {
        this.c = c;
    }

}
