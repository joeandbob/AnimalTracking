public class Point {
    private double r, c;

    public Point(double r, double c) {
        this.r = r;
        this.c = c;
    }

    public double getRow() {
        return r;
    }

    public void setRow(int r) {
        this.r = r;
    }

    public double getCol() {
        return c;
    }

    public void setCol(int c) {
        this.c = c;
    }

}
